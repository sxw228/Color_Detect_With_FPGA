module Driver_Divider
#(parameter Num=16)
(
    input i_clk,
    input i_rst,
    input [Num-1:0]i_dividend,
    input [10:0]i_divisor,
    input [15:0]i_rom_data,
    output [Num-1:0]o_result,
    output [2:0]o_delay_num
    );
    
    //延时参数
    localparam Default_Delay_Num=3'd4;
    
    //数据
    reg [15:0]Rom_Data=0;
    reg [Num+19:0]Tmp_Mul=0;
    
    //缓存
    reg [Num-1:0]dividend_buff=0;
