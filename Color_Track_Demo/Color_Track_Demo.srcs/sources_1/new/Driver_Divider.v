    //输出
    reg [Num-1:0]result_o=0;
    
    //输出连线
    assign o_result=result_o;
    assign o_delay_num=Default_Delay_Num;
    
    //数据输出
    always@(posedge i_clk or negedge i_rst)begin
        if(!i_rst)begin
            result_o<=0;
        end
        else begin
            result_o<=Tmp_Mul[Num+19:20];
        end
    end
    //除数的数据处理
    always@(posedge i_clk or negedge i_rst)begin
        if(!i_rst)begin
            Tmp_Mul<=0;
        end
        else begin
            case(divisor_i)
                11'd0:Tmp_Mul<=0;
                11'd1:Tmp_Mul<={dividend_i,20'd0};
                11'd2:Tmp_Mul<={1'b0,dividend_i,19'd0};
                11'd3:Tmp_Mul<=(dividend_i<<14)+(dividend_i<<12)+(dividend_i<<10)+(dividend_i<<8)+(dividend_i<<6)+(dividend_i<<4)+(dividend_i<<2)+dividend_i;
                11'd4:Tmp_Mul<={2'd0,dividend_i,18'd0};
                11'd5:Tmp_Mul<=(dividend_i<<13)+(dividend_i<<12)+(dividend_i<<9)+(dividend_i<<8)+(dividend_i<<5)+(dividend_i<<4)+(dividend_i<<1)+dividend_i;
                11'd6:Tmp_Mul<=(dividend_i<<15)+(dividend_i<<13)+(dividend_i<<11)+(dividend_i<<9)+(dividend_i<<7)+(dividend_i<<5)+(dividend_i<<3)+(dividend_i<<1);
                11'd7:Tmp_Mul<=(dividend_i<<14)+(dividend_i<<11)+(dividend_i<<8)+(dividend_i<<5)+(dividend_i<<2);
                11'd8:Tmp_Mul<={3'd0,dividend_i,17'd0};
                11'd9:Tmp_Mul<=(dividend_i<<15)+(dividend_i<<14)+(dividend_i<<10)+(dividend_i<<9)+(dividend_i<<8)+(dividend_i<<4)+(dividend_i<<3)+(dividend_i<<2);
                11'd10:Tmp_Mul<=(dividend_i<<15)+(dividend_i<<12)+(dividend_i<<11)+(dividend_i<<8)+(dividend_i<<7)+(dividend_i<<4)+(dividend_i<<3)+dividend_i;
                11'd11:Tmp_Mul<=(dividend_i<<14)+(dividend_i<<13)+(dividend_i<<12)+(dividend_i<<10)+(dividend_i<<6)+(dividend_i<<4)+(dividend_i<<3)+(dividend_i<<2)+dividend_i;
                11'd12:Tmp_Mul<=(dividend_i<<14)+(dividend_i<<12)+(dividend_i<<10)+(dividend_i<<8)+(dividend_i<<6)+(dividend_i<<4)+(dividend_i<<2)+dividend_i;
                11'd13:Tmp_Mul<=(dividend_i<<13)+(dividend_i<<12)+(dividend_i<<11)+(dividend_i<<9)+(dividend_i<<8)+(dividend_i<<4)+(dividend_i<<1)+dividend_i;
                11'd14:Tmp_Mul<=(dividend_i<<13)+(dividend_i<<10)+(dividend_i<<7)+(dividend_i<<4)+(dividend_i<<1);
                11'd15:Tmp_Mul<=(dividend_i<<12)+(dividend_i<<8)+(dividend_i<<4)+dividend_i;
                11'd16:Tmp_Mul<={4'd0,dividend_i,16'd0};
                default:Tmp_Mul<=dividend_i*Rom_Data;
            endcase
        end
    end
    
    //输入缓存
    always@(posedge i_clk or negedge i_rst)begin
        if(!i_rst)begin
            dividend_buff<=0;
            divisor_buff<=11'd1;
            dividend_i<=0;
            divisor_i<=11'd1;
            Rom_Data<=16'd0;
        end
        else begin
            dividend_buff<=i_dividend;
            divisor_buff<=i_divisor;
            dividend_i<=dividend_buff;
            divisor_i<=divisor_buff;
            Rom_Data<=i_rom_data;
        end
    end
endmodule

